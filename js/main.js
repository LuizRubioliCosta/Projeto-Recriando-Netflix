  /* listener scroll header*/

  let lastKnownScrollPosition = 0;
let ticking = false;

function AlteraCor(mudaCor) {
  mudaCor= document.getElementById('container');
  mudaCor.style.backgroundColor ="#141414";
}

document.addEventListener('scroll', function(e) {
  lastKnownScrollPosition = window.scrollY;

  if (!ticking) {
    window.requestAnimationFrame(function() {
      AlteraCor(lastKnownScrollPosition);
      ticking = false;
    });

    ticking = true;
  }
});


